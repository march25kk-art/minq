function renderDiagnosisSeoContent(page) {
  const item = content[page.kind];
  if (!item) return "";
  return `<section id="diagnosisSeoContent" class="diagnosis-seo-content" aria-labelledby="diagnosisSeoHeading">
    <h2 id="diagnosisSeoHeading">${item.heading}</h2>
    <p class="diagnosis-seo-intro">${item.intro}</p>
    <div class="diagnosis-seo-points">${cards(item.points)}</div>
    <h2>${page.title}のよくある質問</h2>
    <div class="diagnosis-faq">${faqs(item.questions)}</div>
    <div class="diagnosis-seo-related"><strong>関連する無料診断</strong>${related(item.related)}</div>
    <p class="diagnosis-seo-policy">掲載内容は自己理解の参考情報です。結果だけで性格・能力・健康状態を断定するものではありません。内容については運営者情報とお問い合わせ窓口を公開しています。</p>
  </section>`;
}

