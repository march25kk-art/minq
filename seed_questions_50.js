const normalize = value => String(value || "")
  .normalize("NFKC")
  .toLowerCase()
  .replace(/[\s\p{P}\p{S}]/gu, "");

const isPossibleDuplicate = (candidate, existing) => {
  const a = normalize(candidate);
  const b = normalize(existing);
  return a === b || (Math.min(a.length, b.length) >= 8 && (a.includes(b) || b.includes(a)));
};

const nowJSTString = offset => {
  const date = new Date(Date.now() + offset);
  return new Intl.DateTimeFormat("ja-JP", {
    timeZone: "Asia/Tokyo",
    year: "numeric", month: "2-digit", day: "2-digit",
    hour: "2-digit", minute: "2-digit", second: "2-digit",
    hour12: false
  }).format(date);
};

async function main() {
  if (questions.length !== 50) throw new Error(`Candidate count must be 50, got ${questions.length}`);

  const snapshot = await firestore.collection("questions").get();
  const existingTitles = snapshot.docs.map(doc => String(doc.data().title || ""));
  const candidateTitles = new Set(questions.map(([title]) => normalize(title)));

  if (process.argv.includes("--verify")) {
    const addedDocs = snapshot.docs.filter(doc => candidateTitles.has(normalize(doc.data().title)));
    const nonZeroVotes = addedDocs.filter(doc => Number(doc.data().totalVotes || 0) !== 0);
    console.log(JSON.stringify({
      mode: "verify",
      totalCount: snapshot.size,
      addedQuestionsFound: addedDocs.length,
      nonZeroVotes: nonZeroVotes.length
    }, null, 2));
    if (addedDocs.length !== 50 || nonZeroVotes.length !== 0) process.exitCode = 1;
    return;
  }

  const duplicatePairs = [];

  for (const [title] of questions) {
    const match = existingTitles.find(existing => isPossibleDuplicate(title, existing));
    if (match) duplicatePairs.push({ title, match });
  }

  if (duplicatePairs.length) {
    console.error(JSON.stringify({ existingCount: snapshot.size, duplicatePairs }, null, 2));
    throw new Error("Possible duplicates found; no questions were added.");
  }

  if (!process.argv.includes("--commit")) {
    console.log(JSON.stringify({ mode: "dry-run", existingCount: snapshot.size, readyToAdd: questions.length }, null, 2));
    return;
  }

  const batch = firestore.batch();
  questions.forEach(([title, description, options, tag], index) => {
    const ref = firestore.collection("questions").doc();
    const time = nowJSTString(index * 1000);
    batch.set(ref, {
      title,
      description,
      options,
      tags: [tag],
      createdAt: time,
      updatedAt: time,
      views: 0,
      totalVotes: 0,
      commentCount: 0,
      reports: 0,
      ip: "system-seed"
    });
  });

  await batch.commit();
  await updateSitemap();

  const after = await firestore.collection("questions").count().get();
  console.log(JSON.stringify({ mode: "committed", added: questions.length, totalCount: after.data().count }, null, 2));
}

main().catch(error => {
  console.error(error);
  process.exitCode = 1;
});
